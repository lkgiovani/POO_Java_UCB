package br.ucb.poo;

public class Casa extends Produto{
	private int manyBathrooms;
	private int manySuites;
	private Boolean manyGarage;
	private int manyBedroom;
	
	public int getManyBathrooms() {
		return manyBathrooms;
	}
	public void setManyBathrooms(int manyBathrooms) {
		this.manyBathrooms = manyBathrooms;
	}
	public int getManySuites() {
		return manySuites;
	}
	public void setManySuites(int manySuites) {
		this.manySuites = manySuites;
	}
	public Boolean getMavyGarage() {
		return manyGarage;
	}
	public void setManyGarage(Boolean manyGarage) {
		this.manyGarage = manyGarage;
	}
	public int getManyBedroom() {
		return manyBedroom;
	}
	public void setManyBedroom(int manyBedroom) {
		this.manyBedroom = manyBedroom;
	}
	
	
}
